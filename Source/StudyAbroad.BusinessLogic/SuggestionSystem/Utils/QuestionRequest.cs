using System.Collections.Generic;
using StudyAbroad.BusinessLogic.SuggestionSystem.Enums;

namespace StudyAbroad.BusinessLogic.SuggestionSystem.Utils
{
    public class QuestionRequest
    {
        public QuestionIdentifierEnum QuestionId { get; set; }
        public string QuestionText { get; set; }
        public List<string> AnswerChoices { get; set; }

        public QuestionRequest(QuestionIdentifierEnum inQuestionId, string inQuestionText, List<string> inAnswerChoices)
        {
            QuestionId = inQuestionId;
            QuestionText = inQuestionText;
            AnswerChoices = inAnswerChoices;
        }
    }
}
